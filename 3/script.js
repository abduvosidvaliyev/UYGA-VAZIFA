let container = document.querySelector(".container")

let inp = document.querySelector(".inp")

let btn1 = document.querySelector(".inputs button")

let index = 0

inp.addEventListener("input", () => {
    forr()
})

function forr() {
    container.innerHTML = ""
    errey.filter(item => item.name.toLowerCase().includes(inp.value.toLowerCase()))
        .forEach(item => {
            let card = document.createElement("div")
            let cardImg = document.createElement("div")
            let cardimg = document.createElement("img")
            let cardName = document.createElement("h4")
            let flexDiv = document.createElement("div")
            let container1 = document.createElement("div")
            let priceText1 = document.createElement("h3")
            let flexText = document.createElement("div")
            let colorText = document.createElement("span")
            let divcolors = document.createElement("div")
            container.appendChild(card)
            card.appendChild(cardImg)
            card.appendChild(container1)
            cardImg.appendChild(cardimg)
            container1.appendChild(cardName)
            container1.appendChild(priceText1)
            container1.appendChild(flexText)
            flexText.appendChild(colorText)
            flexText.appendChild(divcolors)
            card.classList.add("card")
            cardImg.classList.add("cardImg")
            cardimg.classList.add("cardimg")
            cardName.classList.add("cardName")
            flexDiv.classList.add("flexDiv")
            container1.classList.add("containerDiv")
            flexText.classList.add("flexText")
            divcolors.classList.add("divcolors")
            cardimg.src = item.src
            cardName.innerHTML = item.name
            priceText1.innerHTML = `$${item.price}`
            let btn = document.createElement("button")
            btn.innerHTML = "BUY"
            flexText.appendChild(btn)
            let inndex = document.querySelector(".indexx div")
            btn.addEventListener("click", () => {
                index++;
                inndex.innerHTML = index
            })
        });
}

forr()

